package cn.sh.ideal.bean;

public class QuestionAnswerInfo {
	private String klId;
	private String klContentSources;
	private String questionAnswerInfoId;
	private String question;
	private String answerInfo;
	
	public String getKlId() {
		return klId;
	}
	public void setKlId(String klId) {
		this.klId = klId;
	}
	public String getKlContentSources() {
		return klContentSources;
	}
	public void setKlContentSources(String klContentSources) {
		this.klContentSources = klContentSources;
	}
	public String getQuestionAnswerInfoId() {
		return questionAnswerInfoId;
	}
	public void setQuestionAnswerInfoId(String questionAnswerInfoId) {
		this.questionAnswerInfoId = questionAnswerInfoId;
	}
	public String getQuestion() {
		return question;
	}
	public void setQuestion(String question) {
		this.question = question;
	}
	public String getAnswerInfo() {
		return answerInfo;
	}
	public void setAnswerInfo(String answerInfo) {
		this.answerInfo = answerInfo;
	}
}
